export const ArrayItems = (data = []) => {
  if (data?.length > 0) {
    let html = [];
    if (data.length > 2) {
      data.map((x, index, arr) => {
        if (index < 3) {
          html.push(<><span key={index} className={`mt-1 ml-1 mr-1 label font-weight-bold label-lg badge badge-success`}>  {x} </span> </>);
        } else if (index === arr.length - 1) {
          html.push(<span className={` btn btn-sm mt-1 ml-1 mr-1 label font-weight-bold label-lg label-inline `} >  {data?.length - 2} </span>)
        }
      })
    } else {
      data.map((x, index) => {
        html.push(<><span className="mt-1 ml-1 mr-1 label font-weight-bold label-lg badge badge-success" key={index}>  {x} </span> </>)
      })
    }
    return html;
  } else {
    return '';
  }
}


